            <span class="Content_Header">12/4/13 - Breaking News - MCPS to Eliminate Strawberry Milk
            </span>
            <p>
            <span class="Main_Content_Area">
            On Monday we received the incredible news that MCPS is discontinuing the strawberry-flavored milk as of the first of January 2014! This is a big win for the kids in MCPS. Getting rid of the strawberry milk has consistently scored at the top of the list of priorities for RFKM's members, as it contains Red Dye #40, artificial chemical flavor, high fructose corn syrup, propylene glycol and 25 grams of sugar. We say good riddance to the pink milk, and hello to calmer, better-behaved children ready to learn! Our thanks to Marla Caplon, Director of the Division of Food and Nutrition Services for MCPS, for this positive step forward.
            <a href="http://www.washingtonpost.com/local/education/strawberry-milk-is-to-be-banished-from-school-cafeterias-in-montgomery-county/2013/12/09/d9abb83e-5daa-11e3-95c2-13623eb2b0e1_story.html" target="_blank"> The Washington Post</a> reported on it on December 10, 2013 and the
            <a href="http://www.thesentinel.com/mont/Surrender-the-pink12-12-2013" target="_blank"> Montgomery Sentinel</a> on December 12, 2013.&nbsp;
            </span>
            </p>
            <p class="Content_Header">School Food Forum: Creating a Vision of Fresh, Real Food in Montgomery County Public School</p>
            <p class="Main_Content_Area">On November 2, RFKM, in partnership with
            <a href="http://www.montgomeryvictorygardens.org/" target="_blank"> Montgomery Victory
            Gardens</a>, presented a <a href="http://www.realfoodforkidsmontgomery.org/forum.htm"> forum on school food in Montgomery
            County</a>. The event was very well-attended with over 150 participants, including many County Council Members, State Delegates and one State Senator, plus representatives from many other legislative offices.&nbsp;<br>
            The keynote speaker, Tony Geraci, made famous in the documentary <a href="http://www.cafeteriaman.com/" target="_blank"><i> Cafeteria
            Man</i></a>, outlined the many ways in which he engages students in developing food for the school cafeterias, from "no thank you bites" to an agricultural apprenticeship program. A portion of the produce in his Memphis cafeterias comes from farms that he and students construct using hoop houses and imported organic compost. Geraci also explained how he uses technology to save money and give parents control over their children's purchases in the lunch
            room (download <a href="Tony%20Geraci.pptx" target="_blank">Tony's Geraci's Power Point</a>).<br>
            <br>
            Other speakers addressed <a href="Karla%20Kratovil.pptx" target="_blank"> school gardens</a> and
            <a href="Jill%20Coutts.pptx" target="_blank"> agricultural education programs in
            MCPS</a>, unsafe additives in food, and the causes of obesity and diabetes.
            <a href="Kathy%20Lawrence.pptx" target="_blank"> Kathy Lawrence</a> of
            <a href="http://www.schoolfoodfocus.org/" target="_blank"> School Food FOCUS</a> addressed national efforts to change school food through procurement policy changes to promote farm-to-school and their efforts to add less-processed foods, including antibiotic-free chicken, to USDA commodity lists. Marla Caplon, Director of Food and Nutrition Services for MCPS, spoke on the past, present and future of MCPS food. County Councilmembers George Leventhal and Valerie Ervin also spoke about their efforts and aspirations on the topic of school nutrition. Lindsey Parsons, Co-Director of RFKM, delivered the final
            <a href="Call_to_Action.pdf"> Call to Action</a> to close out the forum.<br>
